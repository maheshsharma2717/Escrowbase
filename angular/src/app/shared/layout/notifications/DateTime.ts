export class DateTime {
    year: number;
    month: number;
    day: number;
    hour: number;
    minute: number;
    second: number;
  
    constructor(year: number, month: number, day: number, hour: number, minute: number, second: number) {
      this.year = year;
      this.month = month;
      this.day = day;
      this.hour = hour;
      this.minute = minute;
      this.second = second;
    }
  }